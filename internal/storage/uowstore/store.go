// Package uowstore is a DERISK SPIKE (issue gastownhall/beads#4547, Route A).
//
// It implements a tiny slice of storage.DoltStorage over the unit-of-work
// stack (internal/storage/uow + internal/storage/domain), proving that the
// full "uowStore adapter" sketched in PROPOSAL-uowstore-adapter.md §2 is
// buildable: one adapter type presenting the old store interface, with each
// call becoming one short UOW transaction (exactly the proxied-server design).
//
// UNIMPLEMENTED METHODS RETURN A TYPED ERROR, NEVER PANIC. uowStore embeds the
// generated unsupportedDoltStorage shell (unsupported_gen.go): every method of
// the 144-method interface that this spike does not really override returns a
// *storage.ErrUnsupported naming the operation and this backend, wrapped with
// BD_SPIKE_UOWSTORE / issue #4547 context. That replaces the earlier nil-embed
// shortcut whose untouched methods nil-panicked (SPIKE-REPORT §6.4/§6.6). The
// shell is regenerated by `go generate ./...`; two compile-time assertions in
// the generated file break the build if DoltStorage or Transaction grows a
// method, forcing a regen instead of letting a silent stub answer for it. This
// type must NEVER be shipped as a general store.
