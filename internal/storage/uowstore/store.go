// ---- writes: one short tx via uow.RunInTx with an outcome-derived message ----

// CreateIssue mirrors EmbeddedDoltStore.CreateIssue: infra types route to the
// wisps table, and the caller's *types.Issue is mutated in place with the
// minted ID (the use-case operates on the same pointer via CreateIssueParams).
//
// Label mapping is load-bearing: the embedded store persists issue.Labels (via
// issueops.PersistLabels, which reads the *types.Issue), but the domain create
// use-case reads only params.Labels — so the adapter must copy issue.Labels
// across, or bd create silently drops every label on this path (#4547 red-team).
// issue.Comments is NOT mapped: the create use-case has no comment path, and the
// store-path bd create never populates Comments (comments arrive via bd comment
// add). A full adapter that must round-trip imported comments needs a
// comment-write gap unit — see SPIKE-REPORT §3.
func (s *uowStore) CreateIssue(ctx context.Context, issue *types.Issue, actor string) error {
	if issue == nil {
		return fmt.Errorf("issue must not be nil")
	}
	if err := s.prepareForCreate(ctx, issue); err != nil {
		return err
	}

	params := domain.CreateIssueParams{Issue: issue, Labels: issue.Labels}
	return uow.RunInTxMsg(ctx, s.provider, func(u uow.UnitOfWork) (string, error) {
		var (
			res domain.CreateIssueResult
			err error
		)
		// Route ephemeral AND no-history beads to the wisps table, mirroring
		// EmbeddedDoltStore.CreateIssue's `useWispsTable := Ephemeral || NoHistory
		// || infra`. no_history beads live in wisps with ephemeral=0, so
		// `list --all` (SkipWisps) hides them while `ready` (issues∪wisps union)
		// still surfaces them — the tier set-algebra embedded produces (#4547 F-2).
		// NoHistory does NOT set issue.Ephemeral, so the create JSON is unchanged.
		if issue.Ephemeral || issue.NoHistory {
			res, err = u.IssueUseCase().CreateWisp(ctx, params, actor)
		} else {
			res, err = u.IssueUseCase().CreateIssue(ctx, params, actor)
		}
		if err != nil {
			return "", err
		}
		return fmt.Sprintf("bd: create %s", res.Issue.ID), nil
	})
}

// CloseIssue mirrors EmbeddedDoltStore.CloseIssue: the raw close op only
// (command-level validation — epic children, gate satisfaction, blocker checks
// — lives above the store and is out of scope here). Probes issue-or-wisp so
// the right table is closed, exactly as the proxied close command does.
func (s *uowStore) CloseIssue(ctx context.Context, id string, reason string, actor string, session string) error {
	return mapUowError(uow.RunInTxMsg(ctx, s.provider, func(u uow.UnitOfWork) (string, error) {
		if err := closeIssueInUOW(ctx, u, id, reason, actor, session); err != nil {
			return "", err
		}
		return fmt.Sprintf("bd: close %s", id), nil
	}))
}

