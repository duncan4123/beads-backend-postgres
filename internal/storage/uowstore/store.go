// ---- helpers (package-level so the tx view and the store methods share ONE
// error-mapping/resolution implementation) ----

// getIssueInUOW is the shared body of GetIssue: probe the issues table, fall
// back to wisps, and translate the uow path's wrapped sql.ErrNoRows to the
// store contract's storage.ErrNotFound. Used by (*uowStore).GetIssue AND
// (*uowTransaction).GetIssue so errors.Is(err, storage.ErrNotFound) behaves
// identically inside and outside a transaction.
func getIssueInUOW(ctx context.Context, u uow.UnitOfWork, id string) (*types.Issue, error) {
	issue, err := u.IssueUseCase().GetIssue(ctx, id)
	if err == nil {
		// Hydrate labels from the issues-side label table, matching embedded's
		// issueops.GetIssueInTx (it always populates issue.Labels). The domain
		// use-case GetIssue returns only the row, so without this the close/show
		// return objects carry labels=null even though the labels persist —
		// #4547 F-5. GetLabels routes to `labels` for a regular issue.
		labels, lerr := u.LabelUseCase().GetLabels(ctx, id)
		if lerr != nil {
			return nil, lerr
		}
		issue.Labels = labels
		return issue, nil
	}
	if !isNotFound(err) {
		return nil, err
	}

	wisp, werr := u.IssueUseCase().GetWisp(ctx, id)
	if werr == nil {
		// Wisp-side hydration reads wisp_labels, mirroring GetIssueInTx's
		// issues→wisps table fallback (labels→wisp_labels).
		labels, lerr := u.LabelUseCase().GetWispLabels(ctx, id)
		if lerr != nil {
			return nil, lerr
		}
		wisp.Labels = labels
		return wisp, nil
	}
	if isNotFound(werr) {
		return nil, fmt.Errorf("%w: issue %s", storage.ErrNotFound, id)
	}
	return nil, werr
}

// isWispInUOW reports whether id lives in the wisps table, given an open UOW. It
// distinguishes wisp from regular by probing GetIssue first (matching the
// proxied command's proxiedResolveIssueOrWisp order).
func isWispInUOW(ctx context.Context, u uow.UnitOfWork, id string) (bool, error) {
	if _, err := u.IssueUseCase().GetIssue(ctx, id); err == nil {
		return false, nil
	} else if !isNotFound(err) {
		return false, err
	}
	if _, err := u.IssueUseCase().GetWisp(ctx, id); err == nil {
		return true, nil
	} else if !isNotFound(err) {
		return false, err
	}
	// Not found in either table; let the close use-case surface the canonical
	// not-found error rather than guessing a table here.
	return false, nil
}

// closeIssueInUOW is the shared body of CloseIssue's write closure: probe
// issue-vs-wisp, then route to the matching close use-case with
// domain.CloseIssueParams. Used by (*uowStore).CloseIssue AND
// (*uowTransaction).CloseIssue.
func closeIssueInUOW(ctx context.Context, u uow.UnitOfWork, id, reason, actor, session string) error {
	params := domain.CloseIssueParams{Reason: reason, Session: session}
	isWisp, err := isWispInUOW(ctx, u, id)
	if err != nil {
		return err
	}
	if isWisp {
		_, err = u.IssueUseCase().CloseWisp(ctx, id, params, actor)
	} else {
		_, err = u.IssueUseCase().CloseIssue(ctx, id, params, actor)
	}
	return err
}

// prepareForCreate reproduces the pre-write state EmbeddedDoltStore.CreateIssue
// computes before it opens its write tx, so the spike and the default embedded
// store accept and reject exactly the same `bd create` inputs:
//
//   - Infra-type ephemeral flip. EmbeddedDoltStore.CreateIssue marks every infra
//     type ephemeral UNCONDITIONALLY (create_issue.go:19-21 — note it has no
//     `!issue.NoHistory` guard; that guard lives only on the legacy DoltStore /
//     batch paths). Mirroring that unconditional flip is load-bearing: an infra
//     type created with --no-history must land in the ephemeral+no_history state
//     the validator then declares impossible, so the spike rejects it just like
//     embedded. Guarding the flip here would instead let that combo through as a
//     silent no_history wisp — the opposite of parity.
//   - Issue validation. Embedded validates via issueops.PrepareIssueForInsert ->
//     Issue.ValidateWithCustom (create.go:433); the domain create use-case never
//     does, so without this the spike silently accepts custom-only types like
//     agent/role (types.go custom-only set) and the ephemeral+no_history combo
//     that embedded refuses with exit 1 (#4547 Slice-4 red-team). Custom statuses
//     and types are resolved through the same ConfigUseCase the embedded path
//     reads (custom_types table -> types.custom config -> YAML union), so a
//     genuinely configured custom type still validates on both plumbings.
//
// Runs in one read-only UOW so the flag and the custom status/type sets are
// resolved before the write tx opens (RunInTxMsg may replay fn, so this must stay
// idempotent and pre-computed). The validation error is wrapped exactly as
// PrepareIssueForInsert wraps it so the CLI-visible message is byte-identical.
func (s *uowStore) prepareForCreate(ctx context.Context, issue *types.Issue) error {
	u, err := s.provider.NewUOW(ctx)
	if err != nil {
		return err
	}
	defer u.Close(ctx)

	isInfra, err := u.ConfigUseCase().IsInfraTypeCtx(ctx, issue.IssueType)
	if err != nil {
		return fmt.Errorf("infra-type routing: %w", err)
	}
	if isInfra {
		issue.Ephemeral = true
	}

	customStatuses, err := u.ConfigUseCase().GetCustomStatuses(ctx)
	if err != nil {
		return mapUowError(err)
	}
	customTypes, err := u.ConfigUseCase().GetCustomTypes(ctx)
	if err != nil {
		return mapUowError(err)
	}
	statusNames := make([]string, 0, len(customStatuses))
	for _, st := range customStatuses {
		statusNames = append(statusNames, st.Name)
	}
	if err := issue.ValidateWithCustom(statusNames, customTypes); err != nil {
		return fmt.Errorf("validation failed for issue %s: %w", issue.ID, err)
	}
	return nil
}

// isNotFound covers both the storage-package sentinel and the raw sql.ErrNoRows
// that the domain repositories wrap, so the adapter can translate the uow
// path's not-found convention to the store contract's storage.ErrNotFound.
func isNotFound(err error) bool {
	return errors.Is(err, storage.ErrNotFound) || errors.Is(err, sql.ErrNoRows)
}
